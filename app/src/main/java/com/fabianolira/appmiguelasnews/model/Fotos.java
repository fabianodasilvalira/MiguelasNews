package com.fabianolira.appmiguelasnews.model;

public class Fotos {
    private String algumId;
    private String title;
    private String url;
    private String thumbnairUrl;
    private String id;

    public String getAlgumId() {
        return algumId;
    }

    public void setAlgumId(String algumId) {
        this.algumId = algumId;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getThumbnairUrl() {
        return thumbnairUrl;
    }

    public void setThumbnairUrl(String thumbnairUrl) {
        this.thumbnairUrl = thumbnairUrl;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
